        <h2 class="fr-h3">Le problème</h2>

        <p>
          CartoBio répond à un problème auprès de différents acteurs.
        </p>

        <p>
          <strong>Le grand public, la recherche</strong> : absence d’informations géographiques pour mener à bien des programmes de recherche et de politiques publiques.
          Ces informations sont par exemple nécessaires pour réaliser des études poussées sur l’impact du bio sur la santé, l’environnement (qualité de l’eau, biodiversité) et l’adaptation des politiques agricoles locales.
        </p>

        <p>
          <strong>L’administration</strong> : au niveau national, le MASA souhaite se baser sur des données géographiques pour fiabiliser le paiement des aides PAC bio. Au niveau territorial, les DDT font face à des risques juridiques et au caractère chronophage des décisions qui concernent les aides PAC bio.
        </p>

        <p>
          <strong>Les organismes de certification</strong> : ils regrettent l’absence de supports géographiques pour établir les certificats et les attestations de productions végétales. Cela se traduit par une surcharge de travail lors des audits, ainsi que pendant les périodes de télédéclaration et d’instruction des aides PAC.
        </p>

        <p>
          <strong>Les agriculteurs et agricultrices</strong> : ils sont confrontés à la recopie régulière des mêmes informations, ce qui est chronophage et fastidieux pour eux.
        </p>
      </section>

      <section class="fr-col-md-8">
        <h2 class="fr-h3">La solution apportée</h2>

        <p>
          CartoBio constitue et diffuse des données géographiques du parcellaire bio ; certifiées et interopérables entre les acteurs de la certification en agriculture biologique.
        </p>

        <p>
          Cela prend la forme d’une application web à destination d’agriculteurs, d’agricultrices et des employés d’organismes de certification en agriculture biologique. Elle se synchronise à plusieurs sources de données géographiques pour éviter la recopie d’information (éditeurs de logiciels agricoles, TéléPAC, nCVI…) et favoriser leur diffusion (instructions des aides PAC, observatoires bio, autres aides…).
        </p>

        <p>
          Concernant la diffusion au plus grand nombre, CartoBio met à disposition un jeu de données anonymisées en Open Data.
        </p>
      </section>

